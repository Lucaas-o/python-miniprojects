# Function to ask questions
def ask_question(q):
    print(q["question"])
    for choice in q["choices"]:
        print(choice)
    print("Press 'Q' to quit at any time.")
    answer = input("Your answer (A/B/C/D): ").upper()
    return answer

# Main game loop
def play_game():
    score = 0
    for question in questions:
        answer = ask_question(question)
        
        if answer == 'Q':
            print(f"You quit the game. Your final score is {score} out of {len(questions)}.")
            break
        elif answer == question["correct"]:
            print("Correct!\n")
            score += 1
        else:
            print(f"Wrong! The correct answer was {question['correct']}.\n")

    else:
        # If loop is not broken (user doesn't quit)
        print(f"Your final score is {score} out of {len(questions)}.")

# Start the game
